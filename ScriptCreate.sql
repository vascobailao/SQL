pessoa INT NOT NULL, 
leilao INT NOT NULL,
valor INT NOT NULL, -- valor de cada lance 
PRIMARY KEY (pessoa,leilao,valor),
FOREIGN KEY (pessoa,leilao) REFERENCES concorrente(pessoa,leilao));




